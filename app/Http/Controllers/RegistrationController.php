    {
        $this->validate($request,[
            'first_name'=>'required',
            'last_name'=>'required',
            'email'=>'bail|required|email|unique:users',
            'password'=>'required|confirmed|min:6'
        ]);

        $user = Sentinel::register([
            'first_name'=>$request->first_name,
                'last_name'=>$request->last_name,
                'email'=>$request->email,
                'password'=>$request->password
            ]);
